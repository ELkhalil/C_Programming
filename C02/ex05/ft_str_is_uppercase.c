int	ft_str_is_uppercase(char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		if (src[i] >= 'A' && src[i] <= 'Z')
			i++;
		else
			return (0);
	}
	return (1);
}
